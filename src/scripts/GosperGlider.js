// Copied from https://github.com/Peter-Slump/game-of-life/blob/master/patterns/gosper-glider-gun.txt
const gosperGlider = `........................X...........................................................................
......................X.X...........................................................................
............XX......XX............XX................................................................
...........X...X....XX............XX................................................................
XX........X.....X...XX..............................................................................
XX........X...X.XX....X.X...........................................................................
..........X.....X.......X...........................................................................
...........X...X....................................................................................
............XX......................................................................................
....................................................................................................
....................................................................................................
`;

export let getGosperGlider = () => {
  let lines = gosperGlider.split("\n");
  let cells = [];
  let maxCol = 0;
  for (let l of lines) {
    if (!l.includes("X")) break;

    let row = [];
    maxCol = Math.max(l.lastIndexOf("X"), maxCol);
    for (let i = 0; i < l.length; ++i) {
      row.push(l[i] === "X" ? true : false);
    }
    cells.push(row);
  }
  for (let i = 0; i < cells.length; ++i) {
    cells[i] = cells[i].slice(0, maxCol + 1);
  }
  return cells;
};
